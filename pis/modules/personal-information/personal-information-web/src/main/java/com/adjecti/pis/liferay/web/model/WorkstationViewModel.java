package com.adjecti.pis.liferay.web.model;

public class WorkstationViewModel {
	private String deskNo;               
	private String directNo;             
	private String deskPhoneNo;          
	private String extensionNo;          
	
	public String getDeskNo() {
		return deskNo;
	}
	public void setDeskNo(String deskNo) {
		this.deskNo = deskNo;
	}
	public String getDirectNo() {
		return directNo;
	}
	public void setDirectNo(String directNo) {
		this.directNo = directNo;
	}
	public String getDeskPhoneNo() {
		return deskPhoneNo;
	}
	public void setDeskPhoneNo(String deskPhoneNo) {
		this.deskPhoneNo = deskPhoneNo;
	}
	public String getExtensionNo() {
		return extensionNo;
	}
	public void setExtensionNo(String extensionNo) {
		this.extensionNo = extensionNo;
	}
	
	
	
	
	
	
}
